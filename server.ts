// API: Parse Video Info
app.get("/api/info", (req, res) => {
  const url = req.query.url as string;
  if (!url) {
    return res.status(400).json({ error: "URL is required" });
  }

  const runInfoQuery = (useCookies: boolean) => {
    const settings = getSettings();
    const infoArgs = [
      "--dump-json", 
      "--no-playlist", 
      "--extractor-args", "youtube:player_client=android,mweb,web", 
      "--js-runtimes", "node",
      "--remote-components", "ejs:github"
    ];
    if (settings.proxy) {
      infoArgs.push("--proxy", settings.proxy);
    }
    if (useCookies && fs.existsSync(COOKIES_FILE)) {
      infoArgs.push("--cookies", COOKIES_FILE);
    }
    infoArgs.push(url);

    const ytDlp = spawn("yt-dlp", infoArgs, { env: { ...process.env, PYTHONWARNINGS: "ignore" } });

    let stdoutData = "";
    let stderrData = "";
    let handled = false;

    ytDlp.stdout.on("data", (data) => {
      stdoutData += data.toString();
    });

    ytDlp.stderr.on("data", (data) => {
      stderrData += data.toString();
    });

    ytDlp.on("error", (err: any) => {
      if (handled) return;
      handled = true;
      console.error("yt-dlp info spawn error:", err);
      let errMsg = "Failed to run yt-dlp on the server. Please ensure yt-dlp is installed and in the system's PATH.";
      if (err.code === "ENOENT") {
        errMsg = "The yt-dlp binary was not found on this server. If you deployed this application on Vercel, please note that Vercel is a stateless, serverless environment that does not support running custom CLI binaries or background Python processes like yt-dlp. Please deploy this application to a persistent container/VM platform like Google Cloud Run, Railway, Render, or a VPS.";
      }
      res.status(500).json({ error: errMsg, details: err.message });
    });

    ytDlp.on("close", (code) => {
      if (handled) return;
      handled = true;
      const cleanedStderr = stderrData
        .split("\n")
        .filter(line => !line.includes("Support for Python version 3.10 has been deprecated") && !line.includes("Deprecated Feature: Support for Python"))
        .join("\n")
        .trim();

      const isCookieInvalid = cleanedStderr.includes("cookies are no longer valid") || 
                            cleanedStderr.includes("The provided YouTube account cookies are no longer valid") ||
                            cleanedStderr.includes("rotated in the browser as a security measure");

      if (code !== 0) {
        if (useCookies && isCookieInvalid) {
          console.warn("[Self-Healing] Detected expired/invalid YouTube cookies in info query. Clearing cookies and retrying without cookies...");
          
          try {
            if (fs.existsSync(COOKIES_FILE)) {
              fs.unlinkSync(COOKIES_FILE);
            }
          } catch (e) {}

          const currentSettings = getSettings();
          currentSettings.cookies = "";
          saveSettings(currentSettings);

          // Retry immediately
          return runInfoQuery(false);
        }

        console.error("yt-dlp info error:", cleanedStderr);
        let userFriendlyError = "Failed to load video info.";
        if (cleanedStderr.includes("Incomplete YouTube ID")) {
          userFriendlyError = "Invalid YouTube URL.";
        } else if (cleanedStderr.includes("Private video")) {
          userFriendlyError = "This is a private video and cannot be downloaded.";
        } else if (cleanedStderr.includes("The page could not be found") || cleanedStderr.includes("NOT_FOUND") || cleanedStderr.includes("not found")) {
          userFriendlyError = "The requested video could not be found. Please make sure the URL is correct and the video is public.";
        } else if (isCookieInvalid) {
          userFriendlyError = "Your YouTube cookies have expired or been rotated. Please open YouTube in your browser, export brand-new cookies, paste them in the Settings tab, and click Save.";
        } else if (cleanedStderr.includes("Sign in to confirm your age")) {
          userFriendlyError = "This video is age-restricted and requires authentication.";
        } else if (cleanedStderr.includes("Unable to download webpage")) {
          userFriendlyError = "Network error or invalid link. Please try again.";
        } else if (cleanedStderr.includes("confirm you’re not a bot") || cleanedStderr.includes("confirm you're not a bot") || cleanedStderr.includes("Sign in to confirm")) {
          userFriendlyError = "YouTube is requesting verification to confirm you are not a bot. Please go to the Settings tab and paste your Netscape-format YouTube cookies to authenticate your requests.";
        }
        return res.status(500).json({ error: userFriendlyError, details: cleanedStderr });
      }

      try {
        const json = JSON.parse(stdoutData);
      
      // Map formats
      const formats: FormatInfo[] = (json.formats || []).map((f: any) => {
        let type: 'video' | 'audio' | 'combined' = 'combined';
        if (f.vcodec !== 'none' && f.acodec === 'none') type = 'video';
        if (f.vcodec === 'none' && f.acodec !== 'none') type = 'audio';

        const hasHdr = f.dynamic_range && f.dynamic_range.toLowerCase().includes("hdr");

        return {
          formatId: f.format_id,
          ext: f.ext,
          resolution: f.resolution || (f.width && f.height ? `${f.width}x${f.height}` : null) || (f.vcodec !== 'none' ? "Video" : "Audio"),
          height: f.height || null,
          fps: f.fps || null,
          codec: f.vcodec !== 'none' ? f.vcodec : f.acodec,
          bitrate: f.tbr || null,
          fileSize: f.filesize || f.filesize_approx || null,
          type,
          hdr: hasHdr ? f.dynamic_range : undefined,
          vcodec: f.vcodec && f.vcodec !== 'none' ? f.vcodec : undefined,
          acodec: f.acodec && f.acodec !== 'none' ? f.acodec : undefined,
          asr: f.asr || undefined,
          audioChannels: f.audio_channels || undefined
        };
      });

      // Map subtitles
      const subtitles: string[] = [];
      if (json.subtitles) {
        subtitles.push(...Object.keys(json.subtitles));
      }
      if (json.automatic_captions) {
        subtitles.push(...Object.keys(json.automatic_captions).map(k => `${k} (auto)`));
      }

      const metadata: VideoMetadata = {
        url,
        id: json.id,
        title: json.title,
        channel: json.uploader || json.channel || "Unknown Channel",
        duration: json.duration || 0,
        thumbnail: json.thumbnail || (json.thumbnails && json.thumbnails[0]?.url) || "",
        uploadDate: json.upload_date ? `${json.upload_date.substring(0, 4)}-${json.upload_date.substring(4, 6)}-${json.upload_date.substring(6, 8)}` : "Unknown Date",
        description: json.description || "",
        viewCount: json.view_count || 0,
        formats,
        subtitles: Array.from(new Set(subtitles)),
        isPlaylist: false
      };

      res.json(metadata);
    } catch (e) {
      console.error("JSON parsing error:", e);
      res.status(500).json({ error: "Failed to parse video metadata.", details: e });
    }
    });
  };

  runInfoQuery(true);
});

