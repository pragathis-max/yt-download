// API: Stream downloaded files to the browser
app.get("/api/download/file", (req, res) => {
  const id = req.query.id as string;
  if (!id) {
    return res.status(400).send("ID is required.");
  }

  try {
    const files = fs.readdirSync(DOWNLOADS_DIR);
    const matched = files.find(f => f.includes(`-${id}`));
    if (!matched) {
      return res.status(404).send("Downloaded file not found. It might have been deleted or failed.");
    }

    const filePath = path.join(DOWNLOADS_DIR, matched);
    res.download(filePath, matched.replace(`-${id}`, ''));
  } catch (e) {
    res.status(500).send("Failed to retrieve file.");
  }
});

