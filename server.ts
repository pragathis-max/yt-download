// API: Trigger Download
app.post("/api/download", (req, res) => {
  const {
    id,
    url,
    title,
    thumbnail,
    type,
    resolution,
    format,
    subtitle,
    embedMetadata,
    embedThumbnail,
    downloadThumbnail
  } = req.body;

  if (!url || !id) {
    return res.status(400).json({ error: "Invalid download parameters." });
  }

  // Create active download item
  const settings = getSettings();
  const folder = settings.defaultFolder;
  const downloadItem: DownloadItem = {
    id,
    url,
    title,
    thumbnail,
    type,
    resolution,
    format,
    status: 'queued',
    progress: 0,
    speed: "Waiting...",
    eta: "--:--",
    downloadedSize: "0 B",
    totalSize: "Calculating...",
    remainingSize: "Calculating...",
    folder,
    date: new Date().toLocaleDateString(),
    subtitle: subtitle || 'none',
    embedMetadata: !!embedMetadata,
    embedThumbnail: !!embedThumbnail,
    downloadThumbnail: !!downloadThumbnail
  };

  activeDownloads.set(id, downloadItem);
  notifyProgressClients();

  // Function to actually run the spawned process
  const startProcess = (useCookies: boolean = true) => {
    downloadItem.status = 'downloading';
    downloadItem.speed = "0 B/s";
    notifyProgressClients();

    const args: string[] = [
      "--newline", 
      "--progress", 
      "--extractor-args", "youtube:player_client=android,mweb,web", 
      "--js-runtimes", "node",
      "--remote-components", "ejs:github",
      "--concurrent-fragments", "5", // Multi-threaded chunk downloads for super fast speeds
      "--retries", "infinite", // Automatic retry on network drops
      "--fragment-retries", "infinite" // Automatic fragment retry
    ];

    // Format selection
    if (type === 'audio') {
      if (resolution && resolution !== 'audio' && resolution !== 'best' && !resolution.endsWith('p')) {
        // Specific audio stream format ID selected
        args.push("-f", resolution);
      } else {
        args.push("-f", "bestaudio/best");
      }
      args.push("-x");
      const audioFormat = ['mp3', 'm4a', 'aac', 'wav', 'opus', 'flac'].includes(format) ? format : 'mp3';
      args.push("--audio-format", audioFormat);
      args.push("--audio-quality", "0"); // Force highest audio quality transcode (VBR 0 / CBR 320kbps)
    } else if (type === 'video_only') {
      if (resolution && resolution !== 'best' && !resolution.endsWith('p')) {
        // Specific video stream format ID selected
        args.push("-f", resolution);
      } else {
        const height = resolution.replace('p', '');
        if (height === 'best') {
          args.push("-f", "bestvideo/best");
        } else {
          args.push("-f", `bestvideo[height<=${height}]/best`);
        }
      }
      args.push("--merge-output-format", format);
    } else { // Video + Audio (combined)
      if (resolution && resolution !== 'best' && !resolution.endsWith('p')) {
        // Specific format ID or combination
        if (resolution.includes("+")) {
          args.push("-f", resolution);
        } else {
          // If they selected a single video format ID, merge with best audio
          args.push("-f", `${resolution}+bestaudio/best`);
        }
      } else {
        const height = resolution.replace('p', '');
        if (height === 'best') {
          args.push("-f", "bestvideo+bestaudio/best");
        } else {
          args.push("-f", `bestvideo[height<=${height}]+bestaudio/best[height<=${height}]/best`);
        }
      }
      args.push("--merge-output-format", format);
    }

    // Embed metadata if requested
    if (embedMetadata) {
      args.push("--embed-metadata");
    }

    // Embed thumbnail
    if (embedThumbnail) {
      args.push("--embed-thumbnail");
    }

    // Download separate thumbnail if requested
    if (downloadThumbnail) {
      args.push("--write-thumbnail");
    }

    // Subtitles support
    if (subtitle && subtitle !== 'none') {
      const lang = subtitle.replace(' (auto)', '');
      if (subtitle.endsWith('(auto)')) {
        args.push("--write-auto-subs");
      } else {
        args.push("--write-subs");
      }
      args.push("--sub-langs", lang);
    }

    // Output template
    const filenameTemplate = path.join(DOWNLOADS_DIR, `%(title)s-${id}.%(ext)s`);
    args.push("-o", filenameTemplate);

    // Proxy setting
    if (settings.proxy) {
      args.push("--proxy", settings.proxy);
    }

    if (useCookies && fs.existsSync(COOKIES_FILE)) {
      args.push("--cookies", COOKIES_FILE);
    }

    args.push(url);

    console.log("Spawning yt-dlp with args:", args.join(" "));
    const proc = spawn("yt-dlp", args, { env: { ...process.env, PYTHONWARNINGS: "ignore" } });
    activeProcesses.set(id, proc);

    let outputBuffer = "";
    let downloadHandled = false;

    proc.on("error", (err: any) => {
      if (downloadHandled) return;
      downloadHandled = true;
      console.error("yt-dlp download spawn error:", err);
      downloadItem.status = 'failed';
      downloadItem.speed = "Error";
      let errMsg = "Process failed. Please ensure yt-dlp is installed and in the system's PATH on the server.";
      if (err.code === "ENOENT") {
        errMsg = "The yt-dlp binary was not found. Serverless hosting platforms like Vercel do not support running CLI binaries or background Python processes like yt-dlp. Please deploy on Google Cloud Run, Railway, or Render.";
      }
      downloadItem.error = errMsg;
      notifyProgressClients();

      const history = getHistory();
      history.unshift({
        id,
        url,
        title,
        thumbnail,
        date: new Date().toLocaleDateString(),
        folder: DOWNLOADS_DIR,
        quality: resolution,
        format,
        status: 'failed',
        size: "0 B"
      });
      saveHistory(history);
    });

    proc.stdout.on("data", (data) => {
      const chunk = data.toString();
      outputBuffer += chunk;

      // Split by lines to parse progress
      const lines = chunk.split("\n");
      for (const line of lines) {
        if (!line.trim()) continue;

        // Parse merger status
        if (line.includes("[Merger]") || line.includes("Merging formats")) {
          downloadItem.progress = 98;
          downloadItem.speed = "Merging audio & video...";
          downloadItem.eta = "Finishing...";
          notifyProgressClients();
          continue;
        }

        // Parse extract audio / postprocessor status
        if (line.includes("[ExtractAudio]") || line.includes("Extracting audio")) {
          downloadItem.progress = 99;
          downloadItem.speed = "Extracting audio track...";
          downloadItem.eta = "Finishing...";
          notifyProgressClients();
          continue;
        }

        // Parse normal progress
        // Example: [download]  12.3% of  150.22MiB at  3.52MiB/s ETA 00:39
        if (line.includes("[download]")) {
          const percentMatch = line.match(/([\d\.]+)%/);
          const sizeMatch = line.match(/of\s+([0-9\.]+[a-zA-Z]+)/);
          const speedMatch = line.match(/at\s+([0-9\.]+[a-zA-Z]+\/s)/);
          const etaMatch = line.match(/ETA\s+([\d:]+)/);

          if (percentMatch) {
            downloadItem.progress = parseFloat(percentMatch[1]);
          }
          if (sizeMatch) {
            downloadItem.totalSize = sizeMatch[1];
          }
          
          // Compute downloaded size
          const downloadedSizeMatch = line.match(/\[download\]\s+([\d\.]+[a-zA-Z]+)\s+of/);
          if (downloadedSizeMatch) {
            downloadItem.downloadedSize = downloadedSizeMatch[1];
          } else if (percentMatch && sizeMatch) {
            const pct = parseFloat(percentMatch[1]) / 100;
            const totalStr = sizeMatch[1];
            const sizeVal = parseFloat(totalStr);
            const sizeUnit = totalStr.replace(/[\d\.]+/g, '').trim();
            downloadItem.downloadedSize = `${(sizeVal * pct).toFixed(1)} ${sizeUnit}`;
          }

          if (speedMatch) {
            downloadItem.speed = speedMatch[1];
          } else if (line.includes("has already been downloaded")) {
            downloadItem.progress = 100;
            downloadItem.speed = "Already downloaded";
            downloadItem.eta = "Completed";
            downloadItem.downloadedSize = downloadItem.totalSize;
          }
          if (etaMatch) {
            downloadItem.eta = etaMatch[1];
          }
          notifyProgressClients();
        }
      }
    });

    let stderrBuffer = "";

    proc.stderr.on("data", (data) => {
      const chunk = data.toString();
      stderrBuffer += chunk;
      console.error(`[Download stderr ${id}]:`, chunk);
    });

    proc.on("close", (code) => {
      if (downloadHandled) return;
      downloadHandled = true;
      activeProcesses.delete(id);
      
      if (code === 0) {
        downloadItem.status = 'completed';
        downloadItem.progress = 100;
        downloadItem.speed = "Done";
        downloadItem.eta = "00:00";
        notifyProgressClients();

        // Scan folder for downloaded files with matching ID to find the actual finalized path
        let filePath = "";
        try {
          const files = fs.readdirSync(DOWNLOADS_DIR);
          const matched = files.find(f => f.includes(`-${id}`));
          if (matched) {
            filePath = path.join(DOWNLOADS_DIR, matched);
          }
        } catch (e) {
          console.error("Error scanning output file:", e);
        }

        // Add to history
        const history = getHistory();
        const record: HistoryRecord = {
          id,
          url,
          title,
          thumbnail,
          date: new Date().toLocaleDateString(),
          folder: DOWNLOADS_DIR,
          quality: resolution,
          format,
          status: 'completed',
          size: downloadItem.totalSize || "Unknown Size"
        };
        history.unshift(record);
        saveHistory(history);

        // Keep active downloads in memory but mark as completed
        setTimeout(() => {
          activeDownloads.delete(id);
          notifyProgressClients();
        }, 10000); // clear active dashboard card after 10s

      } else {
        // Only mark as failed if it wasn't cancelled (processes that are paused/cancelled will handle their exit differently)
        if (downloadItem.status !== 'paused' && downloadItem.status !== 'queued') {
          const cleanedStderr = stderrBuffer
            .split("\n")
            .filter(line => !line.includes("Support for Python version 3.10 has been deprecated") && !line.includes("Deprecated Feature: Support for Python"))
            .join("\n")
            .trim();

          const isCookieInvalid = cleanedStderr.includes("cookies are no longer valid") || 
                                cleanedStderr.includes("The provided YouTube account cookies are no longer valid") ||
                                cleanedStderr.includes("rotated in the browser as a security measure");

          if (useCookies && isCookieInvalid) {
            console.warn(`[Self-Healing] Download ${id} failed due to expired/invalid YouTube cookies. Clearing cookies and retrying without cookies...`);
            
            try {
              if (fs.existsSync(COOKIES_FILE)) {
                fs.unlinkSync(COOKIES_FILE);
              }
            } catch (e) {}

            const currentSettings = getSettings();
            currentSettings.cookies = "";
            saveSettings(currentSettings);

            // Re-trigger download without cookies
            setTimeout(() => {
              startProcess(false);
            }, 1000);
            return;
          }

          downloadItem.status = 'failed';
          downloadItem.speed = "Error";
          
          let userFriendlyError = "Process failed. Quality might be unavailable or network cut.";
          
          if (cleanedStderr.includes("The page could not be found") || cleanedStderr.includes("NOT_FOUND") || cleanedStderr.includes("not found")) {
            userFriendlyError = "The requested video or playlist could not be found. Please check if the URL is correct and public.";
          } else if (isCookieInvalid) {
            userFriendlyError = "Your YouTube cookies have expired or been rotated. Please open YouTube in your browser, export brand-new cookies, paste them in the Settings tab, and click Save.";
          } else if (cleanedStderr.includes("confirm you’re not a bot") || cleanedStderr.includes("confirm you're not a bot") || cleanedStderr.includes("Sign in to confirm")) {
            userFriendlyError = "YouTube is requesting bot verification. Please go to the Settings tab and paste your Netscape-format YouTube cookies.";
          } else if (cleanedStderr.includes("Sign in to confirm your age")) {
            userFriendlyError = "This video is age-restricted and requires authentication via cookies in Settings.";
          } else if (cleanedStderr.includes("Private video")) {
            userFriendlyError = "This is a private video and cannot be downloaded.";
          } else if (cleanedStderr.includes("ffmpeg") || cleanedStderr.includes("ffprobe") || cleanedStderr.includes("Postprocessing: merger")) {
            userFriendlyError = "FFmpeg binary is missing or could not merge separate streams. Please install FFmpeg to support high-quality merges.";
          } else if (cleanedStderr.includes("No space left") || cleanedStderr.includes("Disk full") || cleanedStderr.includes("space") && cleanedStderr.includes("write")) {
            userFriendlyError = "Insufficient storage on the hosting environment or disk space full.";
          } else if (cleanedStderr.includes("Requested format is not available") || cleanedStderr.includes("unsupported") || cleanedStderr.includes("format not available")) {
            userFriendlyError = "The requested resolution or format combination is unavailable for this source video. Try choosing an alternative quality.";
          } else if (cleanedStderr.includes("HTTP Error 403") || cleanedStderr.includes("Sign in to confirm you’re not a bot")) {
            userFriendlyError = "YouTube has restricted this request (HTTP 403 / bot verification). Please configure fresh cookies in Settings.";
          } else if (cleanedStderr.includes("Network is unreachable") || cleanedStderr.includes("Connection reset") || cleanedStderr.includes("Timed out")) {
            userFriendlyError = "Network connection interrupted. The download will resume automatically when connectivity is restored.";
          }

          downloadItem.error = userFriendlyError;
          notifyProgressClients();

          // Save failed record in history
          const history = getHistory();
          history.unshift({
            id,
            url,
            title,
            thumbnail,
            date: new Date().toLocaleDateString(),
            folder: DOWNLOADS_DIR,
            quality: resolution,
            format,
            status: 'failed',
            size: "0 B"
          });
          saveHistory(history);
        }
      }
    });
  };

  // Immediate execution for now (respects queue limits depending on client control)
  const simultaneousCount = Array.from(activeDownloads.values()).filter(d => d.status === 'downloading').length;
  if (simultaneousCount >= settings.maxSimultaneousDownloads) {
    downloadItem.status = 'queued';
    downloadItem.speed = "Queued...";
    notifyProgressClients();
  } else {
    startProcess();
  }

  res.json({ success: true, item: downloadItem });
});

// API: Pause / Resume / Cancel Download Control
app.post("/api/download/control", (req, res) => {
  const { id, action } = req.body;
  const item = activeDownloads.get(id);
  const proc = activeProcesses.get(id);

  if (!item) {
    return res.status(404).json({ error: "Download item not found" });
  }

  if (action === 'pause') {
    if (proc) {
      proc.kill("SIGSTOP"); // Native OS pause
      item.status = 'paused';
      item.speed = "Paused";
      notifyProgressClients();
      return res.json({ success: true, status: "paused" });
    }
  } else if (action === 'resume') {
    if (proc) {
      proc.kill("SIGCONT"); // Native OS resume
      item.status = 'downloading';
      item.speed = "Resuming...";
      notifyProgressClients();
      return res.json({ success: true, status: "downloading" });
    } else {
      // If process exited or wasn't running, re-trigger download
      activeDownloads.delete(id);
      return res.json({ success: false, error: "Please retry the download." });
    }
  } else if (action === 'cancel') {
    if (proc) {
      proc.kill("SIGKILL"); // Native OS kill
    }
    activeProcesses.delete(id);
    activeDownloads.delete(id);
    notifyProgressClients();
    return res.json({ success: true, status: "cancelled" });
  }

  res.status(400).json({ error: "Invalid action" });
});

